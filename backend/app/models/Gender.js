const { DataTypes } = require('sequelize');
const sequelize = require('../../config/connectionDB');

const Gender = sequelize.define('Gender', {
    id: {
        type: DataTypes.INTEGER,
        autoIncrement: true,
        primaryKey: true
    },
    name: {
        type: DataTypes.STRING(50),
        allowNull: false,
        unique: true
    }
}, {
    tableName: 'genders',
    timestamps: false
});

module.exports = Gender;